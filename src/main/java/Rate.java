public class Rate {

    public String no;
    public String effectiveData;
    public String mid;
    public String bid;
    public String ask;

    public Rate (String no, String effectiveData, String mid, String bid, String ask){
        this.no = no;
        this.effectiveData = effectiveData;
        this.mid = mid;
        this.bid = bid;
        this.ask = ask;
    }
}
